public class SchedulersTransformer {

    public static <Stream> ObservableTransformer<Stream, Stream> observableIO2Main() {
        return new ObservableTransformer<Stream, Stream>() {
            @Override
            public ObservableSource<Stream> apply(Observable<Stream> upstream) {
                return upstream.subscribeOn(Schedulers.io()).observeOn(AndroidSchedulers.mainThread());
            }
        };
    }
}
